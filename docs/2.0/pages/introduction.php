    <h1>INTRODUCTION</h1>
    <br>

    <h4>I. What is Laravel Backend?</h4>

    <p>Laravel Backend is a lite and lean backend for your next website/web application project.
    It is not only a CMS but more on providing an API for your content to be displayed on frontend. It has a built in set of components
    to get you started easily and fast. It has a full blown USER COMPONENT that manages permissions/groups fully
    integrated with any other component permissions.</p>
    <p>With its simple yet powerful CONTENT BUILDER, you will be able to build different type of contents easily! This contents can be a Blog,
    Catalogue, Pages, Products, Listings and even every content you could ever imagine! Together with its CUSTOM FIELDS for any data you want and its
    TAXONOMIES to group them together, it's awesome!</p>
    <br>
    <h4>II. What makes Laravel Backend different?</h4>

    <p>Laravel Backend is an API-Driven CMS, it does not force you on how you want to implement your site. It
    is extremely flexible and extensible. It has built-in components to get you started. But you may also build
    your own custom components pretty easily with just some short commands that is fully integrated with user permissions
    and on backend navigation.</p>
    <p>This package will allow you to build your application more clean and separated. For example, when making your own custom
    component, it will have its own MVC architecture.</p>
